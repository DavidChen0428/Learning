package Concept;
public class CollectionCompare {
    /*
     *  List :
     *      有順序性(索引值)
     *      內容可以重複
     *      ArrayList
     *      LinkedList
     * 
     *  Set :
     *      沒有順序性
     *      內容無法重複、查找速度快
     *      HashSet
     *      LinkedHashSet
     *      TreeSet
     * 
     *  Map : (不繼承Collection，但常被上兩者拿來比較)
     *      Key-Value特性
     *      Value值可以重複；Key不可重複(新增相同會覆蓋舊有值)
     *      HashMap
     *      TreeMap
     */
}
